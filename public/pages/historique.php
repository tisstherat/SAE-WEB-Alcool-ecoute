            <article>
                <h2>Années 1960</h2>
                <p>
                    <b>1960 -</b> C’est au travers d’un journal, en 1960, relatant une expérience
                    locale d’un groupe d’anciens buveurs (département du Doubs), que d’autres
                    départements (Haute-Savoie, Jura, Vienne, Vosges), s’identifièrent et firent
                    des démarches pour se rencontrer.
                </p>
                <p>
                    <b>1964 –</b> Ces groupes, déclarés en Association dans leur département
                    respectif (les Amis de la Santé et les autres Joie et Santé) décidèrent de
                    leur représentativité nationale en se fédérant. De là est née la :
                    Fédération Nationale des Amis de la Santé – Joie et Santé<br>
                    D’autres Associations départementales sont venues, par la suite, grossir cette
                    Fédération. L’Ile de France a été longtemps l’Association phare de cette
                    Fédération.<br>
                    Une rupture entre les personnes issues des Amis de la Santé et celles venues de
                    Joie et Santé donna naissance à deux groupes associatifs distincts.
                </p>
                <p>
                    <b>1965/1970 –</b> Le groupe de Boulogne Billancourt, sous l’impulsion du docteur
                    Gérard VACHONFRANCE et de Monsieur Yves LAMARQUE, ancien buveur, développa une
                    vaste collaboration avec le service du Docteur HASS, à l’hôpital de Saint Cloud
                    d’une part , et les services d’Hygiène Mentale du département d’autre part.
                </p>
            </article>

            <article>
                <h2>Années 1970</h2>
                <p>
                    <b>1970/1974 –</b> Sous l’impulsion du Docteur Bernard CALAMY, de l’Hygiène Mentale,
                    attaché à l’hôpital de Saint Cloud, avec la collaboration de Joie et Santé, ce
                    circuit fut étendu à l’hôpital de Sèvres aux deux services de médecine générale
                    (hommes et femmes) avec deux groupes de paroles par semaine, et lecture des lettres
                    du Docteur FOUQUET.<br>
                    Ce travail aboutit, à la satisfaction des médecins et du Directeur de l’hôpital, à
                    la création dans cet établissement de cinquante lits d’alcoologie en 1974. Dans la
                    même période, toujours sous l’impulsion du Docteur Bernard CALAMY et de Monsieur
                    LAMARQUE, le réseau Joie et Santé étendit son action à la commune de Meudon et à
                    celle de Colombes sur le modèle précédent.
                </p>
                <p>
                    <b>1979 –</b> Reconnaissance d’utilité Publique.
                </p>
            </article>

            <article>
                <h2>Années 1980 - 1990</h2>
                <p>
                    <b>1985 –</b> La Fédération devient Fédération Nationale Joie et Santé.
                </p>
            </article>

            <article>
                <h2>Années 2000 - 2010</h2>
                <p>
                    <b>2001 –</b> La Fédération Nationale Joie et Santé devient ALCOOL ECOUTE F.N.J.S.
                    (Fédération Nationale Joie et Santé).
                    Les Associations affiliées deviennent Alcool Ecoute Joie et Santé de… .
                </p>
                <p>
                    <b>2012 -</b> Adhésion à la CAMERUP (Coordination des Associations et Mouvements
                    d’Entraide Reconnus d’Utilité Publique).
                </p>
            </article>

